/// Horizontal text alignment: left, center, or right
enum TextAlign { left, center, right }

/// How children are positioned along the main axis
enum MainAxisAlignment { start, center, end, spaceBetween, spaceAround }

/// How children are positioned along the cross axis
enum CrossAxisAlignment { start, center, end, stretch }

/// Layout axis: horizontal (row) or vertical (column)
enum Axis { horizontal, vertical }

/// Text input echo mode: normal, password (bullets), or hidden
enum EchoMode { normal, password, noEcho }

/// Box border drawing style
enum BorderStyle { single, double, rounded, thick }
